// ===== Language Engine =====
let currentLang = localStorage.getItem('rumLang') || 'en';

function setLanguage(lang) {
  currentLang = lang;
  const html = document.documentElement;
  html.lang = lang;
  html.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = translations[lang][el.dataset.i18n];
    if (val !== undefined) el.textContent = val;
  });

  // HTML content (hero h1 with <br> and <span>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = translations[lang][el.dataset.i18nHtml];
    if (val !== undefined) el.innerHTML = val;
  });

  // Toggle button label
  document.getElementById('langLabel').textContent = lang === 'ar' ? 'EN' : 'AR';

  // Flip arrow icon in RTL
  const icon = document.getElementById('aboutBtnIcon');
  if (icon) icon.className = lang === 'ar' ? 'fa-solid fa-arrow-left' : 'fa-solid fa-arrow-right';

  localStorage.setItem('rumLang', lang);
}

// ===== Element References =====
const header = document.getElementById('header');
const menuToggle = document.getElementById('menuToggle');
const navMenu = document.getElementById('navMenu');
const scrollTopBtn = document.getElementById('scrollTop');
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-menu a');

// ===== Language Toggle =====
document.getElementById('langToggle').addEventListener('click', () => {
  setLanguage(currentLang === 'en' ? 'ar' : 'en');
});

// ===== Mobile Menu =====
menuToggle.addEventListener('click', () => {
  navMenu.classList.toggle('active');
  const icon = menuToggle.querySelector('i');
  icon.className = navMenu.classList.contains('active')
    ? 'fa-solid fa-xmark'
    : 'fa-solid fa-bars';
});

navLinks.forEach(link => {
  link.addEventListener('click', () => {
    navMenu.classList.remove('active');
    menuToggle.querySelector('i').className = 'fa-solid fa-bars';
  });
});

// ===== Scroll Events =====
window.addEventListener('scroll', () => {
  const scrollY = window.scrollY;

  header.classList.toggle('scrolled', scrollY > 50);
  scrollTopBtn.classList.toggle('show', scrollY > 400);

  let current = '';
  sections.forEach(section => {
    if (scrollY >= section.offsetTop - 130) current = section.getAttribute('id');
  });
  navLinks.forEach(link => {
    link.classList.remove('active');
    if (link.getAttribute('href') === '#' + current) link.classList.add('active');
  });
});

// ===== Scroll to Top =====
scrollTopBtn.addEventListener('click', () => {
  window.scrollTo({ top: 0, behavior: 'smooth' });
});

// ===== Scroll-in Animations =====
const fadeEls = document.querySelectorAll('.fade-up');
const fadeObserver = new IntersectionObserver(
  entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        fadeObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12, rootMargin: '0px 0px -40px 0px' }
);
fadeEls.forEach(el => fadeObserver.observe(el));

